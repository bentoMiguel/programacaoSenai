L = [5, 7, 2, 9, 4, 1, 3]

print("tamanho da lista;")
print(len(L), "\n")

print("maior valor da lista;")
print(max(L), "\n")

print("menor valor da lista;")
print(min(L), "\n")

print("soma de todos os elementos da lista;")
print(sum(L), "\n")

print("lista em ordem crescente;")
L.sort()
print(L, "\n")

print("lista em ordem decrescente.")
L.reverse()
print(L, "\n")
